package java1;

public class ArithmeticOperators {

	public static void main(String[] args) {
		
		int a = 20;
		int b = 10;
		
		int c;
		System.out.println(c=a+b);
		System.out.println(c=a-b);
		System.out.println(c=a*b);
		System.out.println(c=a/b);
		System.out.println(c=a%b);
	}

}
