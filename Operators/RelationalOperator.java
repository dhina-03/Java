package java1;

public class RelationalOperator {

	public static void main(String[] args) {
		
		int a = 5;
		int b = 9;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);

	}

}
